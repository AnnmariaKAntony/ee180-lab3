    .section .boot, "x"
    .balign 4
    .set    noreorder
    .global boot
    .ent    boot

boot:
    lui     $s0, 0x8002         # status/test reg base
    ori     $s1, $0, 1          # done
    ori     $s2, $0, 1          # overall_ok = 1

###############################################################################
# Helper pattern:
#   xor diff = actual ^ expected
#   sltiu eq = (diff < 1)   => 1 if equal else 0
#   overall_ok &= eq
###############################################################################

###############################################################################
# Test 1: 3 < 5 => 1
###############################################################################
    ori     $t0, $0, 3          # rs
    ori     $t1, $0, 5          # rt
    sltu    $t2, $t0, $t1       # actual

    ori     $t3, $0, 1          # expected
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 2: 7 < 2 => 0
###############################################################################
    ori     $t0, $0, 7
    ori     $t1, $0, 2
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 3: equality => 0
###############################################################################
    ori     $t0, $0, 42
    ori     $t1, $0, 42
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 4: unsigned: 0xFFFFFFFF < 0x00000001 => FALSE (0)
###############################################################################
    lui     $t0, 0xFFFF
    ori     $t0, $t0, 0xFFFF       # rs = 0xFFFFFFFF
    ori     $t1, $0, 1             # rt = 1
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 5: unsigned: 0x00000001 < 0xFFFFFFFF => TRUE (1)
###############################################################################
    ori     $t0, $0, 1
    lui     $t1, 0xFFFF
    ori     $t1, $t1, 0xFFFF       # rt = 0xFFFFFFFF
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 6: boundary: 0 < 0xFFFFFFFF => TRUE (1)
###############################################################################
    addu    $t0, $0, $0
    lui     $t1, 0xFFFF
    ori     $t1, $t1, 0xFFFF
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 7: boundary: 0xFFFFFFFF < 0 => FALSE (0)
###############################################################################
    lui     $t0, 0xFFFF
    ori     $t0, $t0, 0xFFFF
    addu    $t1, $0, $0
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 8: sign-bit treated as unsigned:
#   0x80000000 < 0x7FFFFFFF => FALSE (0) because 0x80000000 is larger unsigned
###############################################################################
    lui     $t0, 0x8000
    ori     $t0, $t0, 0x0000       # rs = 0x80000000
    lui     $t1, 0x7FFF
    ori     $t1, $t1, 0xFFFF       # rt = 0x7FFFFFFF
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 9: reverse:
#   0x7FFFFFFF < 0x80000000 => TRUE (1)
###############################################################################
    lui     $t0, 0x7FFF
    ori     $t0, $t0, 0xFFFF
    lui     $t1, 0x8000
    ori     $t1, $t1, 0x0000
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 10: hazard - rs produced immediately before sltu
#   rs = (1 + 255) = 256, rt = 257 => TRUE
###############################################################################
    ori     $t0, $0, 1
    addiu   $t0, $t0, 255          # rs = 256 (dependency)
    ori     $t1, $0, 257           # rt = 257
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 11: hazard - rt produced immediately before sltu
#   rs = 256, rt = (300 - 1) = 299 => TRUE
###############################################################################
    ori     $t0, $0, 256
    ori     $t1, $0, 300
    addiu   $t1, $t1, -1           # rt = 299 (dependency)
    sltu    $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 12: write to $zero ignored
###############################################################################
    ori     $t0, $0, 1
    ori     $t1, $0, 2
    sltu    $zero, $t0, $t1        # would write 1, but $zero stays 0
    addu    $t2, $zero, $zero
    addu    $t3, $zero, $zero
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Finish
###############################################################################
    sw      $s2, 4($s0)         # test result
    sw      $s1, 0($s0)         # done

$done:
    j       $done
    nop

    .end    boot