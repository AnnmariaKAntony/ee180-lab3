# Description:
#   Comprehensive test for the 'sb' instruction (BIG-ENDIAN).
#
###############################################################################

.section .boot, "x"
.balign 4
.set noreorder
.global boot
.ent boot

boot:

    # status registers
    lui     $s0, 0x8002
    ori     $s1, $0, 1          # done
    ori     $s2, $0, 1          # overall_ok = 1

    # test memory base
    lui     $s3, 0x0000
    ori     $s3, $s3, 0x4000

###############################################################################
# Helper:
#   compare actual vs expected using xor trick
###############################################################################

###############################################################################
# Init word = 0x11223344
###############################################################################
    lui     $t0, 0x1122
    ori     $t0, $t0, 0x3344
    sw      $t0, 0($s3)

###############################################################################
# BIG-ENDIAN byte layout:
#
# addr+0 = 11
# addr+1 = 22
# addr+2 = 33
# addr+3 = 44
###############################################################################

###############################################################################
# TEST 1: store byte at offset 0
#   sb 0xAA → result = AA223344
###############################################################################

    ori     $t1, $0, 0xAA
    sb      $t1, 0($s3)

    lw      $t2, 0($s3)
    lui     $t3, 0xAA22
    ori     $t3, $t3, 0x3344

    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# TEST 2: offset 1 → AA BB 33 44
###############################################################################

    ori     $t1, $0, 0xBB
    sb      $t1, 1($s3)

    lw      $t2, 0($s3)
    lui     $t3, 0xAABB
    ori     $t3, $t3, 0x3344

    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# TEST 3: offset 2 → AA BB CC 44
###############################################################################

    ori     $t1, $0, 0xCC
    sb      $t1, 2($s3)

    lw      $t2, 0($s3)
    lui     $t3, 0xAABB
    ori     $t3, $t3, 0xCC44

    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# TEST 4: offset 3 → AA BB CC DD
###############################################################################

    ori     $t1, $0, 0xDD
    sb      $t1, 3($s3)

    lw      $t2, 0($s3)
    lui     $t3, 0xAABB
    ori     $t3, $t3, 0xCCDD

    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# TEST 5: hazard test (value produced immediately before sb)
###############################################################################

    ori     $t1, $0, 0x80
    addiu   $t1, $t1, -1        # becomes 0x7F

    sb      $t1, 1($s3)

    lw      $t2, 0($s3)
    lui     $t3, 0xAA7F
    ori     $t3, $t3, 0xCCDD

    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# finish
###############################################################################

    sw      $s2, 4($s0)     # result
    sw      $s1, 0($s0)     # done

$done:
    j $done
    nop

.end boot