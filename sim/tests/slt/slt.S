    .section .boot, "x"
    .balign 4
    .set    noreorder
    .global boot
    .ent    boot

boot:
    lui     $s0, 0x8002         # status/test reg base
    ori     $s1, $0, 1          # done
    ori     $s2, $0, 1          # overall_ok = 1

###############################################################################
# Helper pattern:
#   xor diff = actual ^ expected
#   sltiu eq = (diff < 1)   => 1 if equal else 0
#   overall_ok &= eq
###############################################################################

###############################################################################
# Test 1: 3 < 5 => 1
###############################################################################
    ori     $t0, $0, 3          # rs
    ori     $t1, $0, 5          # rt
    slt     $t2, $t0, $t1       # actual

    ori     $t3, $0, 1          # expected
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 2: 7 < 2 => 0
###############################################################################
    ori     $t0, $0, 7
    ori     $t1, $0, 2
    slt     $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 3: -1 < 1 => 1
###############################################################################
    addiu   $t0, $0, -1         # rs = -1
    ori     $t1, $0, 1          # rt = +1
    slt     $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 4: 1 < -1 => 0
###############################################################################
    ori     $t0, $0, 1
    addiu   $t1, $0, -1
    slt     $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 5: negative < negative (true): -10 < -3 => 1
###############################################################################
    addiu   $t0, $0, -10
    addiu   $t1, $0, -3
    slt     $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 6: negative < negative (false): -3 < -10 => 0
###############################################################################
    addiu   $t0, $0, -3
    addiu   $t1, $0, -10
    slt     $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 7: equality case: 42 < 42 => 0
###############################################################################
    ori     $t0, $0, 42
    ori     $t1, $0, 42
    slt     $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 8: boundary values: INT_MIN < INT_MAX => 1
#   INT_MIN = 0x80000000
#   INT_MAX = 0x7FFFFFFF
###############################################################################
    lui     $t0, 0x8000
    ori     $t0, $t0, 0x0000       # rs = INT_MIN
    lui     $t1, 0x7FFF
    ori     $t1, $t1, 0xFFFF       # rt = INT_MAX

    slt     $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 9: boundary reverse: INT_MAX < INT_MIN => 0
###############################################################################
    lui     $t0, 0x7FFF
    ori     $t0, $t0, 0xFFFF       # rs = INT_MAX
    lui     $t1, 0x8000
    ori     $t1, $t1, 0x0000       # rt = INT_MIN

    slt     $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 10: hazard - rs produced immediately before slt
#   rs = (9 - 10) = -1, rt = 0 => -1 < 0 => 1
###############################################################################
    ori     $t0, $0, 9
    addiu   $t0, $t0, -10          # rs becomes -1 (dependency)
    addu    $t1, $0, $0            # rt = 0
    slt     $t2, $t0, $t1

    ori     $t3, $0, 1
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 11: hazard - rt produced immediately before slt
#   rs = 0, rt = (1 - 2) = -1 => 0 < -1 => 0
###############################################################################
    addu    $t0, $0, $0            # rs = 0
    ori     $t1, $0, 1
    addiu   $t1, $t1, -2           # rt becomes -1 (dependency)
    slt     $t2, $t0, $t1

    ori     $t3, $0, 0
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Test 12: write to $zero ignored
###############################################################################
    ori     $t0, $0, 1
    ori     $t1, $0, 2
    slt     $zero, $t0, $t1        # would write 1, but $zero stays 0
    addu    $t2, $zero, $zero
    addu    $t3, $zero, $zero
    xor     $t4, $t2, $t3
    sltiu   $t4, $t4, 1
    and     $s2, $s2, $t4

###############################################################################
# Finish
###############################################################################
    sw      $s2, 4($s0)         # test result
    sw      $s1, 0($s0)         # done

$done:
    j       $done
    nop

    .end    boot